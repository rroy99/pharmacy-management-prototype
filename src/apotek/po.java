    private void updateStock(String kodeobat, int jumlah) {
        try {
            koneksi();
            String sql = "UPDATE obat SET stock = stock + ? WHERE kodeobat = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setInt(1, jumlah);
            pstmt.setString(2, kodeobat);
            pstmt.executeUpdate();

            // Mengambil stok obat yang telah diupdate
            int updatedStock = getObatStock(kodeobat);
            jTextField8.setText(String.valueOf(updatedStock));

            conn.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal menambahkan stok obat" + e);
        }
    }

    private int getObatStock(String kodeobat) throws SQLException {
        String sql = "SELECT stock FROM obat WHERE kodeobat = ?";
        PreparedStatement pstmt = conn.prepareStatement(sql);
        pstmt.setString(1, kodeobat);
        ResultSet rs = pstmt.executeQuery();

        // Mengembalikan nilai stok obat
        if (rs.next()) {
            return rs.getInt("stock");
        } else {
            return 0;
        }
    }
