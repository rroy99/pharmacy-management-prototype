    void cekpo() {
        DefaultTableModel tabelnyo = new DefaultTableModel();
        tabelnyo.addColumn("idorder");
        tabelnyo.addColumn("tanggal po");
        tabelnyo.addColumn("id supplier");
        tabelnyo.addColumn("id apoteker");
        tabelnyo.addColumn("kode obat");
        tabelnyo.addColumn("jumlah");
        tabelnyo.addColumn("total bayar");

        try {
            koneksi();
            String sql = "SELECT idorder, tglpo, po_idsupplier, "
                    + "po_idapoteker, po_kodeobat, po_jumlah, po_totalbayar "
                    + "FROM po JOIN supplier ON po_idsupplier=idsupplier "
                    + "JOIN apoteker ON po_idapoteker=idapoteker "
                    + "JOIN obat ON po_kodeobat=kodeobat "
                    + "WHERE po_idsupplier='" + jTextField1.getText() + "' AND po_idapoteker='" + jTextField3.getText() + "'";
            ResultSet rs = cn.executeQuery(sql);
            while (rs.next()) {
                tabelnyo.addRow(new Object[]{
                    rs.getString(1),
                    rs.getDate(2),
                    rs.getString(3),
                    rs.getString(4),
                    rs.getString(5),
                    rs.getString(6),
                    rs.getString(7)
                });
            }
            tabel1.setModel(tabelnyo);

            // Mengatur nilai tanggal ke jDateChooser
            if (tabelnyo.getRowCount() > 0) {
                Date tglpo = rs.getDate(2);
                jDateChooser1.setDate(tglpo);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "terjadi kesalahan" + e);
        }
    }

