    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        try {
            koneksi();
            int t = tblmodel.getRowCount(); // Mengambil jumlah baris pada tabel model

            String sql = "INSERT INTO po (tglpo, po_idsupplier, po_idapoteker, po_kodeobat, po_jumlah, po_totalbayar) VALUES (NOW(), ?, ?, ?, ?, ?)";

            try (PreparedStatement pstmt = conn.prepareStatement(sql)) {
                for (int i = 0; i < t; i++) {
                    String kodeobat = tblmodel.getValueAt(i, 0).toString();
                    String qty = tblmodel.getValueAt(i, 3).toString();
                    String totalBayar = tblmodel.getValueAt(i, 4).toString();

                    pstmt.setString(1, jTextField1.getText()); // id customer
                    pstmt.setString(2, jTextField3.getText()); // id apoteker
                    pstmt.setString(3, kodeobat);
                    pstmt.setString(4, qty);
                    pstmt.setString(5, totalBayar);

                    pstmt.executeUpdate();
                }
            }

            conn.close();
            bersih();
            tblmodel.setRowCount(0);
            JOptionPane.showMessageDialog(null, "Data Sukses Disimpan");

            // Menampilkan data pada tabel1 setelah simpan
            cekpo();

        } catch (Exception e) {
            e.printStackTrace(); // Menampilkan detail kesalahan pada konsol
            JOptionPane.showMessageDialog(null, "Proses Gagal: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton6ActionPerformed

