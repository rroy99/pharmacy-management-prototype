    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            koneksi();
            String sql = "insert into apoteker values(?,?,?,?,?,?,?,?)";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jComboBox1.getSelectedItem().toString());
            pst.setString(4, jTextField3.getText());
            // Mendapatkan tanggal dari jDateChooser1
            java.sql.Date tanggal = new java.sql.Date(jDateChooser1.getDate().getTime());
            pst.setDate(5, tanggal);
            pst.setString(6, jTextField4.getText());
            pst.setString(7, jTextField5.getText());
            pst.setString(8, jTextField6.getText());

            pst.executeUpdate();
            conn.close();
            tampil();
            bersih();
            // Reset tanggal pada jDateChooser1 setelah simpan
            jDateChooser1.setCalendar(null);
            JOptionPane.showMessageDialog(null, "Data Apoteker berhasil disimpan");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Proses penyimpanan gagal: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

