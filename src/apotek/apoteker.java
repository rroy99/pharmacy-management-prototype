    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int row = 0;
        if (evt.getClickCount() == 1) {
            row = tabel.getSelectedRow();
            jTextField1.setText(tabel.getValueAt(row, 0).toString());
            jTextField2.setText(tabel.getValueAt(row, 1).toString());
            jComboBox1.setSelectedItem(tabel.getValueAt(row, 2).toString());
            jTextField3.setText(tabel.getValueAt(row, 3).toString());
            // Set nilai pada jDateChooser1 setelah klik tabel
            Date date = (Date) tabel.getValueAt(row, 4);
            jDateChooser1.setDate(date);
            jTextField4.setText(tabel.getValueAt(row, 5).toString());
            jTextField5.setText(tabel.getValueAt(row, 6).toString());
            jTextField6.setText(tabel.getValueAt(row, 7).toString());

            jButton1.setEnabled(false);
        }
    }//GEN-LAST:event_tabelMouseClicked

